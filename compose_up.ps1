# Скрипт для запуска сервисов через Docker Compose
# compose_up.ps1

# Команда docker compose up с параметрами:
# -d  : запуск в фоновом режиме (detached)
# --build : принудительная пересборка образов перед запуском
docker compose up -d --build

# Просмотр логов запущенных контейнеров:
# -f : follow (потоковый вывод новых записей)
# Логи будут выводиться в текущую консоль до прерывания (Ctrl+C)
docker compose logs -f


